gen "card2.mp3" "第二个字是「水」。古人看到河里的水在流动，就画了弯弯曲曲的线条，就像水在流动一样。你看，水这个字中间一条竖线像水流，两边的点就像溅起的水花！"

gen "card3.mp3" "第三个字是「火」。古人看到火堆在燃烧，火焰往上跳，就画了一个人字形上面加上火苗的样子。你看，火这个字是不是真的像跳动的火焰？"

