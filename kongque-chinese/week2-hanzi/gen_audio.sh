#!/bin/bash
VOICE="zh-CN-XiaoyiNeural"
RATE="-5%"
DIR="/Users/zq/.openclaw/workspace-housekeeper/sites/kongque-chinese/week2-hanzi/audio"

gen() {
  local f="$1" t="$2"
  if [ ! -f "$DIR/$f" ]; then
    edge-tts --voice "$VOICE" --rate="$RATE" --text "$t" --write-media "$DIR/$f" 2>/dev/null
    echo "✓ $f"
  else
    echo "- $f (exists)"
  fi
}

